using System;
public class ArithmeticOperators
{

  public static void main(string[] args)
  {
    int a;
    a = 1;                      // a would equal 1
    a = a * 10;                 // a would equal 10
    a = a / 2;                  // a would equal 5
    a = a % 2;                  // a would equal 1
    a = a + 4;                  // a would equal 5
    a = a - 1;                  // a would equal 4
    a = a >> 2;                 // a would equal ?
    a = a << 2;                 // a would equal 4
  } 

}
