using System;
public class PrimaryOperators
{

    public static void()
    {

        int a =1;
        Console.Out.WriteLine(“a==2”, a==2);                                    // ?
        Console.Out.WriteLine(“a<2”, a<2);                                        // ? 
        Console.Out.WriteLine(“a>2”, a>2);                                        // ?
        Console.Out.WriteLine(“a<=1”, a<=2);                                    // ?
        Console.Out.WriteLine(“a>=2”, a>=2);                                    // ?
        Console.Out.WriteLine(“a & 1”, a&1);                                     // ?
        Console.Out.WriteLine(“a ^ 1”, a^1);                                       // ?
        Console.Out.WriteLine(“a | 1”, a|1);                                        // ?
        Console.Out.WriteLine(“(a<2) && (a>0)”, ((a<2)&&(a>0)));    // ?
        Console.Out.WriteLine(“(a<2) || (a>0)”, ((a<2)||(a>0)));          // ?

    }

}
